numero=int(input("Ingrese un número del 1 al 12:"))
if numero==12 or numero==1 or numero==2:
    if numero==12:
        print("Diciembre-verano")
    elif numero==1:
        print("Enero-verano")
    else:
        print("Febrero-verano")
if numero==3 or numero==4 or numero==5:
    if numero==3:
        print("Marzo-otoño")
    elif numero==4:
        print("Abril-otoño")
    else:
        print("Mayo-otoño")
if numero==6 or numero==7 or numero==8:
    if numero==6:
        print("Junio-invierno")
    elif numero==7:
        print("Julio-invierno")
    else:
        print("Agosto-invierno")
if numero==9 or numero==10 or numero==11:
    if numero==9:
        print("Setiembre-primavera")
    elif numero==10:
        print("Octubre-primavera")
    else:
        print("Noviembre-primavera")